/*                  

                           Array Methods

unshift() – Adds element(s) to the start of the array.

push() – Adds element(s) to the end of the array.

shift() – Removes the first element from the array.

pop() – Removes the last element from the array.

splice() – Adds/removes element(s) at a specific index.

slice() – Returns a shallow copy of a portion of an array.

indexOf() – Returns the first index of a given value.

includes() – Checks if a value exists in the array.

concat() – Combines two or more arrays.

join() – Converts array to a string, separated by a delimiter.

reverse() – Reverses the array in place.

sort() – Sorts elements alphabetically or by function.

forEach() – Executes a function on each element.

map() – Returns a new array by applying a function to each element.

filter() – Returns a new array with elements that pass a test.

find() – Returns the first element that passes a test.

reduce() – Reduces array to a single value using a function.

toString() – Converts array to a comma-separated string.

at() – Returns element at a specific index (supports negative indexing).

fill() – Fills all or part of the array with a static value.


*/